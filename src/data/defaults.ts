const mapRoutine = (names: string[]): RoutineExercise[] =>
  names.map((name, index) => {
    const exercise = defaultExercises.find((item) => item.name === name || item.id === toId(name));
    if (!exercise) {
      throw new Error(`Missing default exercise: ${name}`);
    }
    return { ...exercise, order: index + 1 };
  });

